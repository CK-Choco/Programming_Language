/*關係運算子運算實作*/ 
#include <stdio.h>
#include <stdlib.h>

int main()
{
	int a=30,b=53;/*宣告兩個整數變數*/
	/*關係運算子運算範例與結果*/
	printf("a=%d b=%d \n",a,b);
	printf("----------------------------\n");
	printf("a>b,比較結果為%d值\n",a>b);	
	printf("a<b,比較結果為%d值\n",a<b);
	printf("a>=b,比較結果為%d值\n",a>=b);
	printf("a<=b,比較結果為%d值\n",a<=b);
	printf("a==b,比較結果為%d值\n",a==b);
	printf("a!=b,比較結果為%d值\n",a!=b);
	
	return 0;
}
